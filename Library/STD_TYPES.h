#ifndef STD_TYPES_H
#define STD_TYPES_H

typedef  unsigned char       uint8;   //%hhu   //%c
typedef  signed char         sint8;    // %hhd //%c

typedef  unsigned short int  uint16;   //hu
typedef  signed  short int   sint16;  //hd

typedef  unsigned long int   uint32;     //lu
typedef  signed long int     sint32;   //%ld

typedef  float               f32; //%f
typedef  double              f64;  //%Lf
typedef  long double         f96;  // %Lf


#endif



